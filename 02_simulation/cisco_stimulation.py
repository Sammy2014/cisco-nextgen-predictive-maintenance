# 4. ANOMALY SCORER — Physics-informed health score
# ──────────────────────────────────────────────────────

def compute_health_score(rdson_current, rdson_baseline, threshold,
                          reliability, rul_steps, total_life_estimate):
    """
    Composite health score [0–100] combining:
      - RdsON degradation ratio
      - Weibull reliability
      - Normalized RUL fraction
    """
    deg_ratio   = min(rdson_current / threshold, 1.0)
    rul_frac    = min(rul_steps / max(total_life_estimate, 1), 1.0) if rul_steps else 0.5
    health      = 0.4 * (1 - deg_ratio) + 0.4 * reliability + 0.2 * rul_frac
    return round(health * 100, 1)


def probable_cause(rdson_pct, health_score):
    """Rule-based probable cause — maps to real failure mechanisms from Papers 1-5."""
    if health_score >= 80:
        return "NOMINAL", "No action required"
    elif health_score >= 60:
        return "WATCH", "Early electron trapping / bond pad micro-stress"
    elif health_score >= 40:
        return "ALERT", "Crack propagation in AlGaN/GaN interface detected"
    elif health_score >= 20:
        return "CRITICAL", "Inverse piezoelectric failure imminent — schedule replacement"
    else:
        return "FAILURE", "Threshold exceeded — immediate intervention required"



# ── CELL 2: Degradation data generator ───────────────────────────────────────
# Three-phase RdsON trajectory (stable → linear drift → exponential runaway)
# Calibrated to NASA MOSFET/GaN profiles (Paper 7, Fig 9)

def generate_degradation(n=500, noise=0.002, thresh=0.10,
                          drift_frac=0.30, seed=42):
    np.random.seed(seed)
    t   = np.arange(n, dtype=float)
    di  = int(drift_frac * n)
    a, b = 0.0002, 7.0
    r_clean = a * (np.exp(b * t / n) - 1)
    r_clean[:di] *= 0.05          # flatten early stable zone
    r = r_clean + noise * np.random.randn(n)
    r = np.clip(r, 0, None)
    fi = int(np.argmax(r >= thresh))
    fi = fi if fi > 0 else n - 1
    return t, r, r_clean, fi, thresh

